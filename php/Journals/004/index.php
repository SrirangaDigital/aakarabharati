			   ಓದು-ಬರಹ ಕಲಿಸುವ ಕೆಲಸವನ್ನು ಸಮರ್ಥವಾಗಿ ನೆಡೆಸುತ್ತಿತ್ತು. ಕನ್ನಡದ ಕಣ್ವರೆಂದೇ ಹೆಸರಾದ ಬಿ.ಎಂ.ಶ್ರೀ. ಅವರು ಇದರ ಮೊದಲ ಅಧ್ಯಕ್ಷರಾಗಿದ್ದರು.</p>
			<p>ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿ ಆಗಿಂದಾಗ್ಗೆ ಹಲವಾರು ವಿಷಯಗಳ ಬಗ್ಗೆ ಸಣ್ಣ ಸಣ್ಣ ಪುಸ್ತಿಕೆಗಳನ್ನು ಹೊರತರುತ್ತಿತ್ತು. ಆ ಕಾಲದಲ್ಲಿ ಆ ಪುಸ್ತಿಕೆಗಳ ಬೆಲೆ ಕೇವಲ ಎರಡು ಆಣೆ (ಈಗಿನ ೧೨ ಪೈಸೆ). ಈ ಪುಸ್ತಿಕೆಗಳು ಹೊಸದಾಗಿ 
			   ಓದಲು ಕಲಿತ ವಯಸ್ಕರು ತಮ್ಮ ಮಿತವಾದ ಓದುವ ಶಕ್ತಿಯನ್ನು ಬಳಸಿ ಓದಿ ಅರ್ಥಮಾಡಿಕೊಳ್ಳಲು ಸಾಧ್ಯವಾಗುವಂತೆ ಇರುತ್ತಿದ್ದುವು. ಹಾಗೆಯೇ 'ನವಸಾಕ್ಷರರು' (ಈ ಶಬ್ದ ಬಳಕೆಗೆ ಬಂದದ್ದು ತೀರ ಇತ್ತೀಚೆಗೆ!) 
			  ತಾವು ಕಲಿತದ್ದನ್ನು ಮರೆಯದಿರಲು ಸಹಾಯಮಾಡುತ್ತಿದ್ದುವು ಮತ್ತು ಅವರ ಜ್ಞಾನಭಂಡಾರವನ್ನೂ ಹೆಚ್ಚಿಸುತ್ತಿದ್ದುವು.</p>
			<p>ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿ ತನ್ನ ಓದುಗರಿಗೆ (ಇವರೆಲ್ಲ 'ನವಸಾಕ್ಷ್ರರು' ಎಂದು ಪ್ರತ್ಯೇಕವಾಗಿ ಹೇಳಬೇಕಿಲ್ಲವಷ್ಟೆ !) ತಮ್ಮ ಸುತ್ತಲಿನ ಪ್ರಪಂಚದ ಆಗುಹೋಗುಗಳ ಬಗ್ಗೆ ಸುಲಭವಾಗಿ ಓದಿ ತಿಳಿಯಲು ಸಹಾಯವಾಗುವಂತಹ ಒಂದು 
			   ವಾರಪತ್ರಿಕೆಯನ್ನು ಹೊರತರುತ್ತಿತ್ತು. 'ಬೆಳಕು' ಅದರ ಹೆಸರು. ಕೇವಲ ನಾಲ್ಕು ಪುಟಗಳ ಆ ಪತ್ರಿಕೆಯಲ್ಲಿ ಆ ವಾರ ಬರುವ ಮುಖ್ಯವಾದ ಹಬ್ಬ, ರೈತರಿಗೆ ಹೆಚ್ಚು ಉಪಯೋಗವಾಗುವ ವಿಷಯ-ಘಟನೆ ಬಗ್ಗೆ ಮಾಹಿತಿ, ರಾಜ್ಯ-
			   ದೇಶ - ಪ್ರಪಂಚದಲ್ಲಿ ಹಿಂದಿನ ವಾರದಲ್ಲಿ ಘಟಿಸಿದ ಪ್ರಮುಖ ವಿಷಯಗಳ ಬಗ್ಗೆ ಸಣ್ಣ ಟಿಪ್ಪಣಿಗಳು, ರೈತರಿಗೆ ವ್ಯವಸಾಯದ ಬಗ್ಗೆ ಏನಾದರೂ ಸಲಹೆ, ಒಂದು ಸಣ್ಣ ಕತೆ - ಇವೆಲ್ಲವೂ ಕೊಂಚ ದಪ್ಪ ಅಕ್ಷರಗಳಲ್ಲಿ, ಹೊಸದಾಗಿ ಓದು ಕಲಿತ 
			  ವಯಸ್ಕರು ಸುಲಭವಾಗಿ ಓದಿ ಅರ್ಥಮಾಡಿಕೊಳ್ಳಬಹುದಾದ ಸರಳವಾದ ಕನ್ನಡದಲ್ಲಿ ಇರುತ್ತಿದ್ದುವು</p>
