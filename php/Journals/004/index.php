			   ಮೊದಲ ವಿಶೇಷ ಸಂಚಿಕೆ ಮೇ ೧೯೪೬ರಲ್ಲಿ ಹೊರಬಂತು. ಅದರ ಮೊದಲ ಆರು ಪುಟಗಳಲ್ಲಿ 'ಸ್ವ-ವಿಷಯ' ಎಂಬ ಶೀರ್ಷಿಕೆಯಲ್ಲಿ ಪ್ರಕಟವಾಗಿರುವ ಲೇಖನದಲ್ಲಿ ಚಿಕ್ಕದಾಗಿ, ಚೊಕ್ಕವಾಗಿ, 'ಪುಸ್ತಕ ಪ್ರಪಂಚ'ದ ಬಗ್ಗೆ ಹೇಳಬೇಕಾದುದೆಲ್ಲವನ್ನೂ ಹೇಳಿದ್ದಾರೆ. ಅದಕ್ಕೆ ಹೇಚ್ಚಿನ ವಿವರಣೆ ಅನಗತ್ಯ.</p>
