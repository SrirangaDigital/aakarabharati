			  ಜನರಿಂದ ದೂರವೇ ಇತ್ತು. ಇಂಥ ಕಾಲದಲ್ಲಿ ಪುಸ್ತಕ ಪ್ರಪಂಚ ಬಂದು ಜನರಿಗೆ ದೊಡ್ಡ ಉಪಕಾರ ಮಾಡಿದ್ದು ಸುಳ್ಳಲ್ಲ.</p>
			<p>ಮೊದಲು ರೂಪಿಸಿದ ಸಂಪಾದಕ ಮಂಡಳಿಯಲ್ಲಿನ ಸದಸ್ಯರ ಹೆಸರುಗಳನ್ನು  ಕೇಳಿದರೆ ಮೈ ಜುಮ್ಮೆನ್ನುತ್ತದೆ. ಅವರ ಪೈಕಿ ಟಿ. ಕೃಷ್ಣಮೂರ್ತಿ, ಎಸ್. ವಿ. ಶ್ರೀನಿವಾಸರಾವ್, ನ.ಭದ್ರಯ್ಯ ಅವರ ಯುನೆಸ್ಕೊ ನೇಮಕದ ಮೇಲೆ ಇದೇ ರಂಗದಲ್ಲಿ ಕೆಲಸಮಾಡಲು 
			   ಹಲವಾರು ವಿದೇಶಗಳಿಗೆ ಹೋಗಿದ್ದರು. ಎಂ. ಆರ್. ಲಕ್ಷಮ್ಮನವರು 'ಬೆಳಕು' ವಾರಪತ್ರಿಕೆಯ ಸಂಪಾದಕರಾಗಿದ್ದರು. ಜಿ. ಹನುಮಂತರಾಯರು ಪ್ರಧಾನ ಸಂಪಾದಕರಾಗಿದ್ದರೂ ಎಸ್. ವಿ. ಶ್ರೀನಿವಾಸರಾಯರನ್ನು ಪುಸ್ತಕ ಪ್ರಪಂಚದ ಸಂಪಾದಕರಾಗಿ ನೇಮಿಸಲಾಗಿತ್ತು.</p>
			<p>ಕಾಲಕ್ರಮದಲ್ಲಿ ಆರ್. ವಿ. ಶ್ರೀನಿವಾಸಮೂರ್ತಿ ಅವರು 'ಬೆಳಕು' ಮತ್ತು 'ಪುಸ್ತಕ ಪ್ರಪಂಚ'ದ ಸಂಪಾದಕರಾದರು. ಇವರು ಆ ವೇಳೆಗಾಗಲೇ ರಾ. ವೆಂ. ಶ್ರೀನಿವಾಸ - ರಾ. ವೆಂ. ಶ್ರೀ. ಎಂಬ ಕಾವ್ಯನಾಮದಲ್ಲಿ ಲೇಖಕರಾಗಿ ಹೆಸರು ಗಳಿಸಿದ್ದರು. ೧೯೬೪ರ ವರೆಗೂ ಇವರು ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿಯಲ್ಲಿ ಸೇವೆ ಸಲ್ಲಿಸಿದರು.</p>
			<p>ಮೊದಲು ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿ ತನ್ನ ಪ್ರಕಟಣೆಗಳನ್ನು ಜಿ. ಹನುಮಂತರಾಯರ ಮಗ ಜಿ. ಹೆಚ್. ರಾಮರಾವ್ ನಡೆಸುತ್ತಿದ್ದ ಮೈಸೂರು ಪ್ರಿಂಟಿಂಗ್ ಅಂಡ್ ಪಬ್ಲಿಷಿಂಗ್ ಹೌಸ್ ಎಂಬ ಮುದ್ರಣಾಲಯದಲ್ಲಿ, ವೆಸ್ಲಿಯನ್ ಮಿಷನ್ ನಡೆಸುತ್ತಿದ್ದ ವೆಸ್ಲಿ ಪ್ರೆಸ್ನಲ್ಲಿ, ಸೀತಾರಾಘವ ಪ್ರೆಸ್, ಪಂಚಾಚಾರ್ಯ ಎಲೆಕ್ಟ್ರಿಕ್ ಪ್ರೆಸ್ - ಹೀಗೆ ಹಲವಾರು ಕಡೆ ಮುದ್ರಿಸುತ್ತಿತ್ತು. ಕಾಲಕ್ರಮದಲ್ಲಿ ಆ ಕಾಲಕ್ಕೆ ಆಧುನಿಕ ಎನ್ನಬಹುದಿದ್ದ ಯಂತ್ರಗಳಿಂದ ಕೂಡಿದ 
			  ತನ್ನದೇ ಪ್ರೆಸ್ ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿಯ ಆವರಣದಲ್ಲೇ ಕಾರ್ಯರಂಭ ಮಾಡಿತು.</p>
			<p>ಪುಸ್ತಕ ಪ್ರಪಂಚದ ಆಶಯಗಳನ್ನು ಸುಲಭವಾಗಿ ಆಗಮಾಡಿಸಲು ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿ ಒಂದು ಉಪಾಯ ಕಂಡುಕೊಂಡಿತ್ತು. ಇಂಗ್ಲಿಷ್ ಮಾಸಿಕ 'ದಿ ರೀಡರ್ಸ್ ಡೈಜೆಸ್ಟ್', ಹಿಂದೀ ಮಾಸಿಕ 'ನವನೀತ್' - ಹೀಗೆ
			 ಹಲವಾರು ಮೂಲಗಳಲ್ಲಿ ಪ್ರಕಟವಾದ ಲೇಖನಗಳನ್ನು ಕನ್ನಡಕ್ಕೆ ಅನುವಾದಿಸಿ ಪ್ರಕಟಿಸಲು ಪೂರ್ವಾನುಮತಿಯನ್ನು ಪಡೆದಿತ್ತು. ಹೀಗಾಗಿ ಯಾರಾದರೂ ಈ ಮೂಲಗಳಿಂದ ಲೇಖನಗಳನ್ನು ಅನುವಾದಿಸಲು ಪ್ರತ್ಯೇಕವಾದ
			 ಅನುಮತಿ ಪಡೆಯುವ ಆವಶ್ಯಕತೆ ಇರಲಿಲ್ಲ. ಇದರಿಂದ ಭಾಷಾಂತರಕಾರರ ಕೆಲಸ ಸುಲಭವಾಗಿತ್ತು.</p>
			<p>ಕನ್ನಡ ಸಾಹಿತ್ಯ ಲೋಕಕ್ಕೆ ಪುಸ್ತಕ ಪ್ರಪಂಚ ಸದ್ದಿಲ್ಲದೆ ದೊಡ್ದ ಸೇವೆ ಸಲ್ಲಿಸಿದೆ ಎಂದರೆ ತಪ್ಪಾಗಲಾರದು. ಈ ಸೇವೆ ಅಷ್ಟು ಪ್ರಸಿದ್ಧಿಗೆ ಬಂದಿಲ್ಲ. ಕನ್ನಡದಲ್ಲಿ ದೊಡ್ಡ ಹೆಸರು ಮಾಡಿರುವ ಹಲವಾರು ಹಳೆಯ ಮೈಸೂರು 
			 ಪ್ರಾಂತ್ಯದ ಲೇಖಕರು ತಮ್ಮ ವಿದ್ಯಾರ್ಥಿದೆಸೆಯಲ್ಲಿ ಪುಸ್ತಕ ಪ್ರಪಂಚಕ್ಕೆ ಲೇಖನಗಳನ್ನು ಬರೆದು ಅದರಿಂದ ಬಂದ ಸಂಭಾವನೆಯನ್ನು ತಮ್ಮ ಸ್ವಂತ ಖರ್ಚು ವೆಚ್ಚುಗಳನ್ನು ಸರಿದೂಗಿಸಲು ಉಪಯೋಗಿಸುತ್ತಿದ್ದರು. ಎಚ್. ತಿಪ್ಪೇರುದ್ರಸ್ವಾಮಿ, 
			 ಸಿ. ಪಿ. ಕೆ., ಆರ್. ತಿಪ್ಪೇಸ್ವಾಮಿ, ಜೀಶಂಪ, ಹಾ. ಮಾ. ನಾಯಕ ಮತ್ತು ಇನ್ನೂ ಹಲವಾರು ಈ ಪಟ್ಟಿಯಲ್ಲಿ ಬರುತ್ತಾರೆ. ಈ ಪೈಕಿ ಹಲವರು ರಾ. ವೆಂ. ಶ್ರೀ. ಅವರಿಗೆ ಇದಕ್ಕಾಗಿ ಕೃತಜ್ಞತೆ ಸಲ್ಲಿಸಿ ಕಾಗದಗಳನ್ನು ಬರೆದಿದ್ದರು.<p>
			<p>ಪುಸ್ತಕ ಪ್ರಪಂಚಕ್ಕೆ ನೇರವಾಗಿ ಸಂಬಂಧಿಸಿದ ಈ ಲೇಖನದಲ್ಲಿ ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿಯ ಕಾರ್ಯವೈಖರಿಯ ಬಗ್ಗೆ ಕೆಲವು ವಿವರಗಳನ್ನು ಕೊಟ್ಟರೆ ಅದು ಅಪ್ರಸ್ತುತವಾಗಲಾರದೆಂದು ಭಾವಿಸುತ್ತೇನೆ. ವಯಸ್ಕರಿಗೆ 
			 ನಡೆಸುವ ಶಾಲೆ ನಡೆಯುತ್ತಿದ್ದದ್ದು ಸಾಧಾರಣವಾಗಿ ಸಂಜೆ ವೇಳೆ. ರೈತಾಪಿ ಜನರು ತಮ್ಮ ಹೊಲ-ಗದ್ದೆ ಕೆಲಸಗಳನ್ನು ಮುಗಿಸಿ ಮನೆಗೆ ಬಂದು ನಂತರ ಶಾಲೆಗೆ-ಕಲಿಕಾ ಕೇಂದ್ರಕ್ಕೆ ಬರುತ್ತಿದ್ದರು. ಆಗ ಲಾಂದ್ರದ ಬೆಳಕಿನಲ್ಲೇ ಅವರ ಚಟುವಟಿಕೆ ನಡೆಯುತ್ತಿತ್ತು.
			 ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿ ಆ ಕೇಂದ್ರಗಳಿಗೆ ಲಾಂದ್ರ, ಸೀಮೇಣ್ಣೆ ಕೂಡ ಒದಗಿಸುತ್ತಿತ್ತು. ಎತ್ತರ ಕಡಿಮೆ, ಅಗಲ ಜಾಸ್ತಿ ಇರುವಂತಹ, ಬಾಗಿಲಿರುವ, ಮರದ ಪುಸ್ತಕ ಕಪಾಟುಗಳನ್ನು ಕೊಡುತ್ತಿತ್ತು. ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿ 
			 ಪ್ರಕಟಿಸುತ್ತಿದ್ದ ಸಣ್ಣ ಪುಸ್ತಿಕೆಗಳಂಥ ನೂರಾರು ಪುಸ್ತಕಗಳನ್ನು ಅದರಲ್ಲಿ ಇಡಬಹುದಾಗಿತ್ತು. ನೆಲದ ಮೇಲೆ ಕುಳಿತುಕೊಂಡೇ ಕಪಾಟಿನಿಂದ ಬೇಕಾದ ಪುಸ್ತಕಗಳನ್ನು ಈಚೆ ತೆಗೆಯಲು ಅನುಕೂಲವಾಗಿತ್ತು.</p>
			<p>ಇದಲ್ಲದೆ ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿ ಹಲಾವಾರು ಕಡೆ ವಿದ್ಯಾಪೀಠಗಳನ್ನು ನಡೆಸುತ್ತಿತ್ತು. ನಂಜನಗೂಡಿನಲ್ಲಿ, ಮಂಡ್ಯ ಜಿಲ್ಲೆಯ ಶಿವಾರಗುಡ್ಡದಲ್ಲಿ, ಶಿವಮೊಗ್ಗ ಜಿಲ್ಲೆಯಲ್ಲಿ ಇದ್ದ ವಿದ್ಯಾಪೀಠಗಳು ಬಹಳ ಹೆಸರು ಮಾಡಿದ್ದುವು.</p>
			<p>ನಾ. ಕಸ್ತೂರಿಯವರು ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಪ್ರಸಾರಕ್ಕೆ ಹಳ್ಳಿಗಳಿಗೆ ಪ್ರವಾಸ ಮಾಡಿ ಹರಿಕಥೆಯಂತೆ ಕಥಾಕಾಲಕ್ಷೇಪ ಮಾಡಿ ಅನಕ್ಷರಸ್ಥ ಜನರನ್ನು ಅಕ್ಷರ ಕಲಿಯುವಂತೆ ಪ್ರೇರೇಪಿಸುತ್ತಿದ್ದರು. ಹಲವಾರು ಸಾರಿ ಜೈಲಿಗೆ ಭೇಟಿ ನೀಡಿ ಅಲ್ಲಿದ್ದ ಖೈದಿಗಳಿಗೆ ಅಕ್ಷರ ಕಲಿಯುವಂತೆ ಹುರಿದುಂಬಿಸುತ್ತಿದ್ದರು. ಇಂಥ ಒಂದು ಕರ್ಯಕ್ರಮದಲ್ಲಿ 
			 ಡಾ. ಎಚ್. ಕೆ. ರಂಗನಾಥ್ ಅವರನ್ನು ಕರೆದುಕೊಂಡು ಹೋಗಿದ್ದರ ಬಗ್ಗೆ ಕಸ್ತೂರಿ ಬರೆಯುತ್ತಾರೆ. ಇತ್ತೀಚೆಗೆ ನಿಧನರಾದ ಹೆಸರಾಂತ ವ್ಯಂಗ್ಯಚಿತ್ರಕಾರ ಆರ್. ಕೆ. ಲಕ್ಷ್ಮಣ್ ಕೂಡ ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿಯ ಪುಸ್ತಿಕೆಗಳಿಗೆ 
			 ರೇಖಾಚಿತ್ರಗಳನ್ನು ಬರೆದುಕೊಡುತ್ತಿದ್ದರು. </p>
			<p>ಸರ್ಕಾರದಿಂದ ಅನುದಾನ ಪಡೆಯುತ್ತಾ ನಡೆಯುತ್ತಿದ್ದ ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿಯ ಮೇಲೆ ಹಲವರ ಪಟ್ಟಭದ್ರ ಹಿತಾಸಕ್ತಿಯುಳ್ಳವರ ಕಣ್ಣು ಬಿತ್ತು. ಅದುವರೆಗೂ ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿಯ ನೌಕರರಿಗೆ ಪಿಂಚಣಿ ಸೌಲಭ್ಯವಿರಲಿಲ್ಲ. ಇವೆಲ್ಲ ಕಾರಣಗಳ 
			ಹಿನ್ನಲೆಯಲ್ಲಿ ಒಂದು ದಿನ ರಾಜ್ಯ ಸರ್ಕಾರ ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿಯನ್ನು ತನ್ನ ವಶಕ್ಕೆ ತೆಗೆದುಕೊಂಡಿತು. ಅದಕ್ಕೊಬ್ಬ ಛೇರ್ಮನ್, ಅವರಿಗೆ ಒಂದು ಕಾರು, ಮನೆ ಇತ್ಯಾದಿಗಳು ಹುಟ್ಟಿಕೊಂಡವು. ಅಧಿಕಾರದಲ್ಲಿರುವ ಪಕ್ಷದ ಧುರೀಣರಿಗೆ ಹಂಚಲು ಇರುವ ಹಲವಾರು ಹುದ್ದೆಗಳ ಪಟ್ಟಿಯಲ್ಲಿ 'ಕರ್ನಾಟಕ ರಾಜ್ಯ ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಸಮಿತಿ' ಯೂ ಸೇರಿತು. ಹೊಸ ಯೋಜನೆಗಳು ಹುಟ್ಟಿಕೊಂಡವು. ಹಣದ ಹೊಳೆ ಹರಿಯಿತು. ವಯಸ್ಕರ ಶಿಕ್ಷಣ ಹಿಂದಕ್ಕೆ ಸರಿಯಿತು. </p>
			<p>ಈ ಗೊಂದಲಗಳ ನಡುವೆ ಪುಸ್ತಕ ಪ್ರಪಂಚದ ರೂಪ ಬದಲಾಯಿತು. ಹೊಸ ಸಂಪಾದಕ ಮಂಡಳಿಯ ನೇಮಕವಾಯಿತು. ಆಧುನಿಕ ಮುದ್ರಣ ಯಂತ್ರಗಳು ಬಂದವು. ಆದರೆ ಅದರ ಮುಖ್ಯಸ್ಥರ ಜಾಗದಲ್ಲಿ ಬಂದವರೆಲ್ಲರಿಗೂ ತಮ್ಮ ಕೆಲಸಕ್ಕೆ ಇರಬೇಕದ ನಿಷ್ಠೆ ಮತ್ತು ಬದ್ಧ್ತೆಯ ಕೊರತೆ ಎದ್ದು ಕಾಣುತ್ತಿತ್ತು. ಹೀಗಾಗಿ ಪುಸ್ತಕ  ಪ್ರಪಂಚ ಚರಿತ್ರೆಯ ಪುಟಗಳಿಗೆ ಸರಿದು ಹೋಯಿತು. ಇವತ್ತಿನ ದಿನಗಳಲ್ಲಂತೂ ಪುಸ್ತಕ ಪ್ರಪಂಚ ತನ್ನ ಪ್ರಸ್ತುತತೆಯನ್ನು ಸಂಪೂರ್ಣವಾಗಿ ಕಳೆದುಕೊಂಡಿದೆ ಎಂದರೆ ತಪ್ಪಾಗಲಾರದು. </p>
