			   ನಂಬಬೇಕಿತ್ತು. ವಿದೇಶದಲ್ಲಿ, ಅಷ್ಟೇಕೆ, ದೇಶದ ಬೇರೆ ಭಾಗಗಳಲ್ಲಿ ಪ್ರಕಟವಾದ ಪುಸ್ತಕಗಳ ಬಗ್ಗೆ ಮಾಹಿತಿ ಸಿಗುತ್ತಿರಲಿಲ್ಲ, ಪುಸ್ತಕಗಳು ಸಿಗುವುದಂತೂ ಇನ್ನೂ ಕಷ್ಟ. ೫೦ರ ದಶಕದಲ್ಲಿ ಕೂಡ ವಿದ್ಯಾರ್ಥಿಗಳು ಲೈಬ್ರೆರಿಯಿಂದ ಪುಸ್ತಕಗಳನ್ನು
